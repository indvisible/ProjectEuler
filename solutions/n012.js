function n12_v3(){
    console.time('timer');

    function divisors(x) {
        var limit = Math.sqrt(x);
        var numberOfDivisors = 0;

        for (var i = 0; i < limit; i++) {
            if (x % i == 0) {
                limit = x / i;
                if (limit != i) {
                    numberOfDivisors++;
                }

                numberOfDivisors++;
            }
        }

      return numberOfDivisors;
    }

    var triangleNumber, previousNumber, index, counter, powIndex;
    previousNumber = 0;
    powIndex = 1/3;
    for(counter = 1; index <= 500 ; counter++){
        triangleNumber = previousNumber + counter;

        if (Math.pow(triangleNumber, powIndex) < 250 || triangleNumber % 2 !== 0 || triangleNumber % 3 !== 0 || triangleNumber % 4 !== 0) {
            previousNumber = triangleNumber;
            continue;
        };

        index = divisors(triangleNumber);
        previousNumber = triangleNumber;
    }

    console.timeEnd('timer');
    return 'answer: ' + triangleNumber;
}

